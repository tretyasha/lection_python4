# Функция zip 
# Функция zip() применяется к набору итерируемых объектов и возвращает итератор с кортежами из элементов входных данных

# zip ([1,2,3],['о','д','т'],['f','s','t']) т.е. мы в ф-ию zip передаем 3 списка
# [(1,'o','f'),(2,'д','s'),(3,'т','t')] и на выходе мы получаем список,в котором мы взяли первый элемент из первого списка,второго списка и из третьего и получили кортеж

# на выходе получаем набор данных,состоящих из элементов соответствующих исходному набору.
# На выходе получаем набор данных, состоящий из элементов соответствующих
# исходному набору.

